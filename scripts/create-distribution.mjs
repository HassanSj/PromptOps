/**
 * Builds distribution/out/ — legal + docs only (no application source).
 * For public copyright footprint without publishing full code.
 */
import fs from "node:fs"
import path from "node:path"
import { fileURLToPath } from "node:url"

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, "..")
const out = path.join(root, "distribution", "out")

const toCopy = ["LICENSE", "NOTICE", "COPYRIGHT"]

const readmeDist = `# PromptOps — distribution bundle

This folder is generated by \`npm run distribution\`. It contains **copyright
and legal notices only**, plus this readme — **not** the PromptOps application
source code.

- For **development**, clone the full private repository instead.
- To regenerate this bundle from the full repo, run \`npm run distribution\` at
  the repository root.

Ensure LICENSE / NOTICE / COPYRIGHT match your legal entity before publishing
this bundle anywhere public.
`

const manifestHeader = `PromptOps distribution manifest
Generated: ${new Date().toISOString()}
These files are intentionally limited; application source is not included.
`

fs.rmSync(out, { recursive: true, force: true })
fs.mkdirSync(out, { recursive: true })

for (const name of toCopy) {
  const src = path.join(root, name)
  if (!fs.existsSync(src)) {
    console.warn(`skip missing: ${name}`)
    continue
  }
  fs.copyFileSync(src, path.join(out, name))
}

fs.writeFileSync(path.join(out, "README-DISTRIBUTION.md"), readmeDist, "utf8")

const files = fs.readdirSync(out).sort()
fs.writeFileSync(
  path.join(out, "MANIFEST.txt"),
  `${manifestHeader}\n${files.join("\n")}\n`,
  "utf8"
)

console.log(`Wrote ${files.length} items to ${path.relative(root, out)}`)
